export const PARTS_BY_CATEGORY = PARTS.reduce((acc, part) => {
  if (!acc[part.category]) acc[part.category] = []
  acc[part.category].push(part)
  return acc
}, {} as Record<string, Part[]>)

export function findPartsByNames(text: string): Part[] {
  const lower = text.toLowerCase()
  return PARTS.filter(p =>
    lower.includes(p.name.toLowerCase()) ||
    lower.includes(p.fullName.toLowerCase()) ||
    (lower.includes(p.brand.toLowerCase()) && lower.includes(p.name.split(' ')[0].toLowerCase()))
  )
}

